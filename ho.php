<ol>
	<div id="aca">
	<li><b><h2>Acacia Honey</h2></b><p>Acacia is one of the most popular honey varieties. Identified as light and clear honey, it takes it delicate floral taste from the nectar of the blossoms of Robinia pseudo acacia, also known as Black Locust in North America and Europe. The high concentration of fructose helps acacia honey to retain its liquid state for a long period of time. Its low sucrose content makes it widely popular among the diabetics and with as a rich source of anti-inflammatory properties, it is best suited to treat respiratory disorders.</p>
		</li>
	</div>

    <div id="alf">
	<li><b><h2>Alfalfa Honey</h2></b><p>Alfalfa honey is majorly produced in Canada and the United States. Made from the purple or blue blossoms, it is light in color and has a mild floral aroma and taste.<br>Due to its sweet yet mild taste, Alfalfa honey is often used for baking purposes. Though honey-enthusiasts prefer eating it straight out of the jar, it works better when combined with other ingredients like tea, lemonade and milkshakes among others.</p>
	</li>
</div>

<div id="ast">
	<li><b><h2>Aster Honey</h2></b><p>This light-colored honey is majorly extracted from the Mid-South region of the United States. Aster Honey has a thick and smooth consistency accompanied by a distinctively sweet smell.<br>It tends to crystallize faster than most other variety of Honey available and tastes best when used as a natural sweeter in a variety of sweet drinks and desserts.</p></li>
</div>

<div id="avo">
	<li><b><h2>Avocado Honey</h2></b><p>Avocado Honey doesn’t taste anything like the fruit, but is extracted from the California avocado blossoms. It is a dark colored honey with a rich and buttery flavor and makes for a tasty salad dressing or condiment when mixed with other ingredients.</p></li>
</div>

<div id="bas">
	<li><b><h2>Basswood Honey</h2></b><p>Basswood Honey is most popular for its biting taste, distinctive white color and exceptional malleability quality that makes teaming it with any food item an easy job. Basswood Honey is produced from the cream-colored Basswood blossoms found throughout North America. It's fresh, pleasant and wood-like essence tastes incredibly well with a variety of teas and is hugely recommended for salad dressings and marinades.</p></li></div>

<div id="bee">
	<li><b><h2>Beechwood Honey</h2></b><p>Popularly known as Honeydew honey, it is sourced from New Zealand's South Island. It comes from the sap produced by aphids on the bark of the Beechwood tree and later collected by the bees. Beechwood Honey is widely used as syrup for pancakes and fruit salads due to its aromatic properties.<br>Its regular consumption is also known to improve the body’s immunity, better the digestive system and is often used as a popular supplement due to its high nutritional value.</p></li></div>

<div id="blu">
	<li><b><h2>Blueberry Honey</h2></b><p>A pleasant flavor variety produced in New England and in Michigan, Blueberry honey is extracted from the white flowers of the blueberry bush. Light amber colored, a well-rounded flavor and a hint of tanginess from the Blueberry. Blueberry Honey makes for an ideal accompaniment for your breakfast meals like oats, pancakes and cereal bars.</p></li>
	</div>

	<div id="bl">
	<li><b><h2>Bluegum Honey</h2></b><p>This eucalyptus honey specie majorly grows in South Australia and Tasmania. Its dense texture and amber-color makes it a go-to topping for toasts and wafers. 8) Bluegum Honey’s taste comes quite close to that of a bubble gum with a subtly cool and a minty undertone beneath its real dense texture.</p></li></div>

<div id="buc">
	<li><b><h2>Buckwheat Honey</h2></b><p>The strongest and darkest of honey varieties, Buckwheat Honey is mostly produced in Minnesota, Ohio, and Pennsylvania as well as in few eastern regions of Canada. As a rich source of iron and other essential nutrients, Buckwheat Honey is among the most popular and widely consumed varieties of honey.<br>It contains more antioxidant compounds than some lighter varieties of Honey in the same category.</p></li></div>

<div id="clo">
	<li><b><h2>Clover Honey</h2></b><p>Clover Honey is one of the most widely available and popular honey varieties in the world. It is majorly produced across Canada and New Zealand.<br>Clover honey has been termed as a classic because of its pleasant and floral sweet taste, making it a hit ingredient for a vast variety of sauces and salad dressings.</p></li></div>

<div id="dan">
	<li><b><h2>Dandelion Honey</h2></b><p>Dandelion Honey is a relatively stronger variety of honey with mild blends of sourness in it. It is widely produced in New Zealand's South Island and is usually dark-amber in color. Dandelion Honey bears a characteristic Dandelion aroma, which is considered as a medicinal herb in China, Tibet and India due to its healing properties.</p></li>
</div>

<div id="euc">
	<li><b><h2>Eucalyptus Honey</h2></b><p>Originally originated in Australia, Eucalyptus Honey is widely extracted in California as well. A Great medicinal honey variety, Eucalyptus is traditionally used by people across the globe for protection against cold and headaches.<br>Due to its extensive availability, Eucalyptus Honey differs in taste and flavor but carries a characteristic herbal flavor and a slight aftertaste of menthol.</p></li></div>

<div id="fir">
	<li><b><h2>Fireweed Honey</h2></b><p>Fireweed Honey comes from a tall herb grown in the open woods of north-west US. Light in color, it has its own way of being sweet and complex at the same time. Its smooth, delicate and buttery taste makes it a great option for gourmet cooking, baking and fish smoking.</p></li></div>

<div id="heat">
	<li><b><h2>Heather Honey</h2></b><p>One of the strongest and most pungent flavors available, Heather’s aftertaste talks about being almost bitter and is commonly used as a spread or marinating Ham, chicken, lamb, seafood and cold meat dishes.<br>Heather Honey is thick and amber in color and is known to be a rich source of protein.</p></li></div>

<div id="iro">
	<li><b><h2>Ironbark Honey</h2></b><p>Another premium Eucalyptus floral variety (Eucalyptus crebra) with a bold taste, Iron Bark Honey is extracted from the blossoms throughout the year in eastern Australia.<br>A favorite flavor catalyst for baking and barbecued meats, Ironbark Honey’s amber color and dense texture adds a decadent taste to smoothies as well as other sweet drinks and shakes.</p></li></div>

<div id="jar">
	<li><b><h2>Jarrah Honey</h2></b><p>Jarrah is a dark- amber eucalyptus variety of honey that has a caramel aftertaste. Jarrah Honey has been scientifically researched to find out that Jarrah is an effective remedy for wounds, burns and skin allergies due to the higher percentage of activity level than most of the honey variety available in the market.</p></li></div>

<div id="leat">
    <li><b><h2>Leatherwood Honey</h2></b><p>Leatherwood Honey comes from the Leatherwood blossom in the south-west region of Tasmania, Australia. Leatherwood Honey is known for its unique taste and strong flavor. Also, commonly known as Tasmanian Honey, it has established itself as an irreplaceable ingredient that not only sweetens but also adds an excellent flavor to cakes, muffins, coffee and tea.</p></li></div>

<div id="lin">
    <li><b><h2>Linden Honey</h2></b><p>Light yellow in colour, Linden Honey bears a very delicate and fresh woody scent. One of the most medicinally rich varieties of honey, Linden has been a favorite for many before bedtime. Its sedative properties help immensely in the cases of anxiety and insomnia. Linden Honey is also used in the treatment of cold, cough and bronchitis.<br>Extracted from Linden tree, which bears small yellowish white flowers, Linden honey is majorly produced in Denmark.</p></li></div>

<div id="mac">
    <li><b><h2>Macadamia Honey</h2></b><p>Macadamia Honey was first sourced in Australia from the floral nectar of the Macadamia Nut tree. It is deep in colour with a complex aroma and has a subtle nutty flavour. Macadamia Honey works well with salads, ice- cream, toasts and even as marinate for grilled chicken wings.</p></li></div>

<div id="man">
    <li><b><h2>Manuka Honey</h2></b><p>Native to New Zealand's coastal areas, Manuka Honey is collected from the flower of the Tea Tree bush. It comprises of rich antibacterial property that aids in effectively treating stomach ulcers, sore throat, cold, indigestion and acne & pimples among others. Manuka Honey’s taste varies due to the difference in source but usually boasts of a robust aftertaste that stays for a while.</p></li></div>

<div id="ora">
    <li><b><h2>Orangeblossom Honey</h2></b><p>Orangeblossom Honey, often a combination of citrus sources, is usually light in colour and mild in flavour with a fresh fruity scent, and a fragrant citrusy taste. Originally originated from Spain/ Mexico, today Orangeblossom Honey is being produced in many countries including Florida, Southern California and Texas among others.</p></li></div>

<div id="pin">
    <li><b><h2>Pinetree Honey</h2></b><p>Pinetree Honey or Honeydew mainly comes from Greece. Quite opposite to the usual sweet taste of honey, Pinetree Honey has a slightly bitter taste with a strong aroma and is very rich in minerals and proteins.</p></li></div>

<div id="sou">
    <li><b><h2>Sourwood Honey</h2></b><p>Sourwood Honey is light-colored, delicate honey variety of Honey with a caramel kind of taste. While many find its taste to be sour, its hardcore followers find it to be as sweet and nutty as any other variety of Honey available.</p></li></div>

<div id="sag">
    <li><b><h2>Sage Honey</h2></b><p>The light-colored and heavy-bodied Sage Honey is mostly produced in California. Sage Honey is packed with a property to granulate very slowly and is commonly used to blend with other honeys to slow down the process of granulation.<br>Its mild yet delightful taste also makes it hugely popular with the consumers.</p></li></div>

<div id="tup">
    <li><b><h2>Tupelo Honey</h2></b><p>One of the most premium honeys ever produced, Tupelo Honey or the “Southern Gold”, as it is popularly known as, is produced in the Southeastern U.S. swamps. It is usually light golden or amber in color with a faint greenish glow and a mild & distinctive taste.<br>Tupelo honey is one of the sweetest honey varieties, courtesy the high fructose content and its ability of not granulating like most other types of Honey.</p></li></div>

